<h1> PERSONERIA ESTUDIANTIL </h1>
<h2>FUNCIONES PERSONERIA ESTUDIANTIL </h2>
<h5>1- Promover el cumplimiento de los derechos y deberes de los estudiantes, para lo cual podrá utilizar los medios de comunicación interna del establecimiento, pedir la colaboración del consejo de estudiantes, organizar foros u otras formas de deliberación.</h5>
<h5>2- Recibir y evaluar las quejas y reclamos que presenten los educandos sobre lesiones a sus derechos y las que formule cualquier persona de la comunidad sobre el incumplimiento de las obligaciones de los alumnos.<h5>
<h5>3- Presentar ante el rector o el Director Administrativo, según sus peticiones, las solicitudes que considere necesarias para proteger los derechos de los estudiantes y facilitar el cumplimiento de sus deberes.<h5>
<h5>4- Cuando considere necesario apelar ante el consejo directivo o el organismo que haga sus veces, las decisiones del rector respecto a las peticiones presentadas por su intermedio.<h5>
<h5>5- Permanente defensa de los intereses de los estudiantes.</h5>
<h5>6- Promover y velar porque el estudiante actúe con libertad y respeto de conciencia.</h5>
<h5>7- Estar atento al cumplimiento del manual de Convivencia.</h5>
<h5>8- Asesorar con criterio serio al estudiante que sea sancionado para que interponga los recursos ante las autoridades del Plantel Educativo.</h5>
<h5>9- Instruir debidamente a los estudiantes que manifiesten la intención de formular alguna petición o queja.</h5>
<h5>10- Promover la ejecución de las leyes, disposiciones administrativas y Manual de Convivencia, que se refieran a la organización y actividad del establecimiento Educativo.</h5>
